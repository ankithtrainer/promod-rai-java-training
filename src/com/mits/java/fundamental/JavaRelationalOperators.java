package com.mits.java.fundamental;

public class JavaRelationalOperators {
    public static void main(String[] args) {

        int x = 10;
        int y =20;
        // == operator
        System.out.println(x==y);  // false

        // != operator
        System.out.println( x != y); // true

        // > operator
        System.out.println( x> y);  // false

        System.out.println( x<y ); // true

        System.out.println( x>= y ); // false

        System.out.println( x <= y); // true


    }
}
